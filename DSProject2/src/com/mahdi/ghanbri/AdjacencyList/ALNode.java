package com.mahdi.ghanbri.AdjacencyList;

public class ALNode {
    private ALNode next;
    private int data;

    public ALNode getNext() {
        return next;
    }

    public void setNext(ALNode next) {
        this.next = next;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

}
